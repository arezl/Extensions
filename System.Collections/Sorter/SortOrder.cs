namespace System.Collections.Sorter
{
    /// <summary>
    ///   Specifies the sorting order.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        ///   Sort in ascending order.
        /// </summary>
        Ascending = 0,

        /// <summary>
        ///   Sort in descending order.
        /// </summary>
        Descending = 1
    }
}